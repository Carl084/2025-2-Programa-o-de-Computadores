#Solicito a distancia percorrida e o tempo gasto.
DistanceTravel = float(input('Qual a distancia percorrida em Km?'))
TimeSpent = float(input('Qual foi o tempo gasto na viagem em horas?'))

#Calculo a velocidade media.
AverageSpeed = DistanceTravel/TimeSpent

#Print do resultado.
print(f'A velocide media foi de {AverageSpeed}Km/hr.')